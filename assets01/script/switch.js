let age = parseInt(prompt("Сколько Вам лет?"));
if (age == 5) {
    alert('Позовите родителей, пожалуйста!');
} else if (age == 10) {
    alert('Вы школьник! Делайте уроки!');
} else if (age == 20) {
    alert('Поздравляем, Вы студент! Отличной учёбы!');
} else if (age == 30) {
    alert('На работу!');
} else if (age == 70) {
    alert('Поздравляем! Вы пенсионер!');    
} else {
    alert('Мы не знаем, что Вам делать...');
} 